package com.cpp2.domain;

import java.util.Date;


public class Movie {
	
	private int id;
	private String moviename;
	private String director;
	private Date showtimes;
	private String runtime;
	private String castActor;
	private String language;
	private String style;
	private String area;
	private String type;
	private String introduction;
	private double price;
	private String status;
	private String image;
	private int popularity;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMoviename() {
		return moviename;
	}
	public void setMoviename(String moviename) {
		this.moviename = moviename;
	}
	public String getDirector() {
		return director;
	}
	public void setDirector(String diretor) {
		this.director = diretor;
	}
	public Date getShowtimes() {
		return showtimes;
	}
	public void setShowtimes(Date showtimes) {
		this.showtimes = showtimes;
	}
	public String getRuntime() {
		return runtime;
	}
	public void setRuntime(String runtime) {
		this.runtime = runtime;
	}
	public String getCastActor() {
		return castActor;
	}
	public void setCastActor(String castActor) {
		this.castActor = castActor;
	}
	public String getLanguage() {
		return language;
	}
	public void setLanguage(String language) {
		this.language = language;
	}
	public String getStyle() {
		return style;
	}
	public void setStyle(String style) {
		this.style = style;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getIntroduction() {
		return introduction;
	}
	public void setIntroduction(String introduction) {
		this.introduction = introduction;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public int getPopularity() {
		return popularity;
	}
	public void setPopularity(int popularity) {
		this.popularity = popularity;
	}
	
	
	
	
	
}
